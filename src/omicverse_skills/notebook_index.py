def index_path() -> Path:
    """Return the absolute path to the shipped ``notebook_index.json``."""
    return Path(__file__).resolve().parent / INDEX_FILENAME


def load_index(path: Optional[os.PathLike] = None) -> dict:
    """Read the shipped notebook index. Override ``path`` for a custom file."""
    p = Path(path) if path is not None else index_path()
    return json.loads(p.read_text(encoding="utf-8"))


def md5_of(path: Path) -> str:
    """Hex-encoded MD5 of the file at ``path``. Reads in 64 KiB chunks so
    even multi-MB notebooks don't materialise in memory."""
    h = hashlib.md5()
    with path.open("rb") as fh:
        for chunk in iter(lambda: fh.read(1 << 16), b""):
            h.update(chunk)
    return h.hexdigest()


def fs_mtime_iso(path: Path) -> str:
    """ISO-8601 UTC mtime of ``path`` (seconds resolution)."""
    ts = path.stat().st_mtime
    return (_dt.datetime
            .fromtimestamp(ts, tz=_dt.timezone.utc)
            .isoformat(timespec="seconds"))


def _git_run(repo: Path, args: list[str]) -> str:
    """Run ``git -C repo args``; return stripped stdout, or '' on failure."""
    try:
        return subprocess.check_output(
            ["git", "-C", str(repo), *args],
            text=True, stderr=subprocess.DEVNULL,
        ).strip()
    except subprocess.CalledProcessError:
        return ""


def git_last_commit(tutorials_repo: Path, rel_path: str) -> dict[str, str]:
    """Return ``{sha, date_iso}`` for the last commit that touched
    ``rel_path`` inside ``tutorials_repo``."""
    out = _git_run(tutorials_repo,
                   ["log", "-1", "--format=%H\t%cI", "--", rel_path])
    if not out or "\t" not in out:
        return {"sha": "", "date_iso": ""}
    sha, date_iso = out.split("\t", 1)
    return {"sha": sha, "date_iso": date_iso}


def git_repo_head(tutorials_repo: Path) -> dict[str, str]:
    sha       = _git_run(tutorials_repo, ["rev-parse", "HEAD"])
    date_iso  = _git_run(tutorials_repo, ["log", "-1", "--format=%cI"])
    return {"sha": sha, "date_iso": date_iso}


def _basename_to_relpath(tutorials_repo: Path) -> dict[str, str]:
    """For every notebook found under ``tutorials_repo/docs/Tutorials-*/``,
    map its basename ``"t_foo.ipynb"`` → relative POSIX path
    ``"docs/Tutorials-X/t_foo.ipynb"``. Notebooks under
    ``.ipynb_checkpoints`` or ``_build`` are ignored."""
    out: dict[str, str] = {}
    docs = tutorials_repo / "docs"
    for d in TUTORIAL_DIRS:
        sub = docs / d
        if not sub.exists():
            continue
        for nb in sub.rglob("t_*.ipynb"):
            if any(part in (".ipynb_checkpoints", "_build") for part in nb.parts):
                continue
            out[nb.name] = nb.relative_to(tutorials_repo).as_posix()
    return out


