"""Skill ↔ tutorial-notebook index for omicverse-skills.

The shipped ``notebook_index.json`` records which omicverse-tutorials
notebook(s) each skill covers, along with each notebook's md5,
filesystem mtime, and last-touching git commit. When the upstream
tutorials repo updates a notebook, ``check_freshness`` flags the
skills whose recorded fingerprints have drifted, so the skill can be
re-reviewed.

Public API
----------
- :data:`SKILL_NOTEBOOK_MAP` — the canonical skill → list-of-notebook-
  basenames mapping. Edit here when you add a new skill.
- :func:`load_index` — read ``notebook_index.json`` shipped inside the
  package; returns the parsed dict.
- :func:`build_index` — re-compute md5 / mtime / git info for every
  skill in :data:`SKILL_NOTEBOOK_MAP` against a given
  ``omicverse-tutorials`` checkout and write a fresh JSON.
- :func:`check_freshness` — compare current notebook fingerprints
  against the shipped JSON; return a list of stale entries.
- :func:`format_freshness_report` — human-readable rendering of
  :func:`check_freshness` output.

CLI
---
``python -m omicverse_skills.notebook_index build --tutorials <path>``
``python -m omicverse_skills.notebook_index check  --tutorials <path>``
``python -m omicverse_skills.notebook_index show   [skill-name]``
"""
