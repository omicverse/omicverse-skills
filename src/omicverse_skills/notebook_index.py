def _resolve_tutorials_arg(arg: Optional[str]) -> Path:
    """Pick a sensible default for the tutorials repo path."""
    if arg:
        p = Path(arg).expanduser().resolve()
        if not p.exists():
            raise SystemExit(f"--tutorials path does not exist: {p}")
        return p
    env = os.environ.get("OMICVERSE_TUTORIALS_REPO")
    if env:
        p = Path(env).expanduser().resolve()
        if p.exists():
            return p
    raise SystemExit(
        "Specify the tutorials repo via --tutorials <path> "
        "or the OMICVERSE_TUTORIALS_REPO env var.\n"
        f"Canonical URL: {TUTORIALS_REPO_URL}"
    )


def _cli() -> None:
    p = argparse.ArgumentParser(
        prog="python -m omicverse_skills.notebook_index",
        description="Skill ↔ tutorial-notebook index management.",
    )
    sub = p.add_subparsers(dest="cmd", required=True)

    p_build = sub.add_parser("build", help="Recompute the index from a tutorials checkout")
    p_build.add_argument("--tutorials", help="Path to omicverse-tutorials checkout")
    p_build.add_argument("--out",       help="Where to write the JSON (default: in-package)")

    p_check = sub.add_parser("check", help="Check freshness of the shipped index")
    p_check.add_argument("--tutorials", help="Path to omicverse-tutorials checkout")
    p_check.add_argument("--no-git",    action="store_true", help="Skip per-file git log lookup")
    p_check.add_argument("--json",      action="store_true", help="Emit machine-readable JSON")

    p_show = sub.add_parser("show", help="Print the shipped index for a skill (or all)")
    p_show.add_argument("skill", nargs="?", help="Skill name; omit for full index")

    args = p.parse_args()

    if args.cmd == "build":
        repo = _resolve_tutorials_arg(args.tutorials)
        out  = build_index(repo, args.out)
        s = out["summary"]
        print(f"Wrote {args.out or index_path()}")
        for k in ("n_skills", "n_skills_with_nb", "n_skills_no_nb",
                  "n_notebook_entries", "n_distinct_notebooks_referenced",
                  "n_notebooks_in_repo", "n_notebooks_unreferenced_by_any_skill"):
            print(f"  {k}: {s[k]}")
        return

    if args.cmd == "check":
        repo = _resolve_tutorials_arg(args.tutorials)
        stale = check_freshness(repo, use_git=not args.no_git)
        if args.json:
            print(json.dumps([e.to_dict() for e in stale], indent=2,
                              ensure_ascii=False))
        else:
            print(format_freshness_report(stale))
        sys.exit(1 if stale else 0)

    if args.cmd == "show":
        idx = load_index()
        if args.skill is None:
            print(json.dumps(idx, indent=2, ensure_ascii=False))
            return
        if args.skill not in idx.get("skills", {}):
            raise SystemExit(f"Unknown skill: {args.skill}")
        print(json.dumps(idx["skills"][args.skill], indent=2,
                          ensure_ascii=False))
        return


if __name__ == "__main__":   # pragma: no cover
    _cli()
