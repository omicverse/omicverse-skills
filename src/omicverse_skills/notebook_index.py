def build_index(
    tutorials_repo: os.PathLike,
    out_path: Optional[os.PathLike] = None,
    skill_map: Optional[Mapping[str, list[str]]] = None,
) -> dict:
    """Compute md5 / mtime / git info for every skill's notebooks and
    write a fresh JSON.

    Parameters
    ----------
    tutorials_repo : path
        Filesystem path to a checkout of the omicverse-tutorials repo
        (the directory containing ``docs/Tutorials-*/``).
    out_path : path, optional
        Where to write the JSON. Defaults to the in-package
        ``notebook_index.json``.
    skill_map : dict, optional
        Override the canonical mapping (defaults to
        :data:`SKILL_NOTEBOOK_MAP`).

    Returns
    -------
    dict
        The serialised index (also written to ``out_path``).
    """
    repo = Path(tutorials_repo).expanduser().resolve()
    if not (repo / "docs").exists():
        raise FileNotFoundError(
            f"omicverse-tutorials repo not found at {repo} (no docs/ subdir)"
        )
    skill_map = dict(skill_map if skill_map is not None else SKILL_NOTEBOOK_MAP)

    base_to_rel = _basename_to_relpath(repo)
    head        = git_repo_head(repo)

    skills_out: dict[str, dict] = {}
    for sk_name in sorted(skill_map):
        records: list[dict] = []
        unresolved: list[str] = []
        for base in skill_map[sk_name]:
            rel = base_to_rel.get(base)
            if rel is None:
                unresolved.append(base)
                continue
            nb_full = repo / rel
            git_info = git_last_commit(repo, rel)
            records.append({
                # `path` is recorded WITHOUT the .ipynb suffix so it matches
                # the canonical readthedocs URL stem; the .ipynb is implied.
                "path":                rel.removesuffix(".ipynb"),
                "md5":                 md5_of(nb_full),
                "fs_mtime_utc":        fs_mtime_iso(nb_full),
                "git_last_commit":     git_info["sha"],
                "git_last_commit_utc": git_info["date_iso"],
            })
        entry: dict = {"notebooks": records}
        if unresolved:
            entry["unresolved_basenames"] = unresolved
        skills_out[sk_name] = entry

    referenced = {nb["path"] for v in skills_out.values()
                  for nb in v["notebooks"]}
    all_basenames = set(base_to_rel)
    nbs_unref = sorted(
        b.removesuffix(".ipynb")
        for b in (all_basenames - {Path(p).name + ".ipynb" for p in referenced})
    )
    n_total_entries = sum(len(v["notebooks"]) for v in skills_out.values())

    out = {
        "version":  1,
        "tutorials_repo": {
            "url":              TUTORIALS_REPO_URL,
            "url_legacy":       TUTORIALS_REPO_URL_LEGACY,
            "git_commit":       head["sha"],
            "git_commit_utc":   head["date_iso"],
        },
        "checked_at_utc": _dt.datetime.now(tz=_dt.timezone.utc)
                              .isoformat(timespec="seconds"),
        "summary": {
            "n_skills":            len(skills_out),
            "n_skills_with_nb":    sum(1 for v in skills_out.values()
                                       if v["notebooks"]),
            "n_skills_no_nb":      sum(1 for v in skills_out.values()
                                       if not v["notebooks"]),
            "n_notebook_entries":  n_total_entries,
            "n_distinct_notebooks_referenced": len(referenced),
            "n_notebooks_in_repo": len(all_basenames),
            "n_notebooks_unreferenced_by_any_skill": len(nbs_unref),
            "notebooks_unreferenced_by_any_skill":   nbs_unref,
        },
        "skills": skills_out,
    }
    target = Path(out_path) if out_path is not None else index_path()
    target.write_text(json.dumps(out, indent=2, ensure_ascii=False) + "\n",
                      encoding="utf-8")
    return out


@dataclass
class FreshnessEntry:
    """One drift record produced by :func:`check_freshness`."""

    skill:           str
    notebook_path:   str            # without .ipynb suffix
    status:          str            # 'changed' | 'missing' | 'unresolved'
    recorded_md5:    str = ""
    current_md5:     str = ""
    recorded_mtime:  str = ""
    current_mtime:   str = ""
    recorded_commit: str = ""
    current_commit:  str = ""

    def to_dict(self) -> dict:
        return {k: v for k, v in self.__dict__.items() if v != ""}


def check_freshness(
    tutorials_repo: os.PathLike,
    index: Optional[dict] = None,
    *,
    use_git: bool = True,
) -> List[FreshnessEntry]:
    """Compare the shipped JSON against a current omicverse-tutorials
    checkout. Returns a list of skills/notebooks whose fingerprint has
    drifted (md5 changed, file missing, etc.).

    Parameters
    ----------
    tutorials_repo : path
        Path to a checkout of omicverse-tutorials.
    index : dict, optional
        A pre-loaded index dict; if ``None``, loads from the shipped
        ``notebook_index.json``.
    use_git : bool, default True
        If False, skips the per-file ``git log -1`` lookup (faster).

    Returns
    -------
    list of FreshnessEntry
        Empty list ⇒ everything is in sync.
    """
    repo = Path(tutorials_repo).expanduser().resolve()
    idx = index if index is not None else load_index()
    drift: List[FreshnessEntry] = []

    for skill, info in idx.get("skills", {}).items():
        for nb in info.get("notebooks", []):
            nb_rel = nb["path"] + ".ipynb"
            nb_full = repo / nb_rel
            if not nb_full.exists():
                drift.append(FreshnessEntry(
                    skill=skill, notebook_path=nb["path"],
                    status="missing",
                    recorded_md5=nb.get("md5", ""),
                ))
                continue
            cur_md5 = md5_of(nb_full)
            if cur_md5 != nb.get("md5"):
                e = FreshnessEntry(
                    skill=skill, notebook_path=nb["path"],
                    status="changed",
                    recorded_md5=nb.get("md5", ""),
                    current_md5=cur_md5,
                    recorded_mtime=nb.get("fs_mtime_utc", ""),
                    current_mtime=fs_mtime_iso(nb_full),
                )
                if use_git:
                    cur = git_last_commit(repo, nb_rel)
                    e.recorded_commit = nb.get("git_last_commit", "")
                    e.current_commit  = cur["sha"]
                drift.append(e)
        for base in info.get("unresolved_basenames", []) or []:
            drift.append(FreshnessEntry(
                skill=skill, notebook_path=base,
                status="unresolved",
            ))

    return drift


def format_freshness_report(stale: Iterable[FreshnessEntry]) -> str:
    """Pretty-print :func:`check_freshness` output for a terminal."""
    lines: list[str] = []
    by_status: dict[str, list[FreshnessEntry]] = {}
    for e in stale:
        by_status.setdefault(e.status, []).append(e)

    for status in ("changed", "missing", "unresolved"):
        items = by_status.get(status, [])
        if not items:
            continue
        lines.append(f"=== {status.upper()} ({len(items)}) ===")
        for e in items:
            lines.append(f"  [{e.skill}]  {e.notebook_path}")
            if status == "changed":
                lines.append(f"      recorded md5: {e.recorded_md5}")
                lines.append(f"      current  md5: {e.current_md5}")
                if e.recorded_commit and e.current_commit:
                    lines.append(f"      recorded commit: {e.recorded_commit[:12]}")
                    lines.append(f"      current  commit: {e.current_commit[:12]}")
        lines.append("")

    if not lines:
        return "All notebooks match the shipped index. ✓"
    return "\n".join(lines).rstrip()


